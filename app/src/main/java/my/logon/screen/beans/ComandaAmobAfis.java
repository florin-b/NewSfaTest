package my.logon.screen.beans;

public class ComandaAmobAfis {

	private String id;
	private String idAmob;
	private String dataCreare;
	private String valoare;
	private String moneda;
	private String numeClient;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getIdAmob() {
		return idAmob;
	}

	public void setIdAmob(String idAmob) {
		this.idAmob = idAmob;
	}

	public String getDataCreare() {
		return dataCreare;
	}

	public void setDataCreare(String dataCreare) {
		this.dataCreare = dataCreare;
	}

	public String getValoare() {
		return valoare;
	}

	public void setValoare(String valoare) {
		this.valoare = valoare;
	}

	public String getMoneda() {
		return moneda;
	}

	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}

	public String getNumeClient() {
		return numeClient;
	}

	public void setNumeClient(String numeClient) {
		this.numeClient = numeClient;
	}

}
