package my.logon.screen.beans;

public class BeanLocalitate {

	private String localitate;
	private boolean isOras;
	private int razaKm;
	private String coordonate;

	public String getLocalitate() {
		return localitate;
	}

	public void setLocalitate(String localitate) {
		this.localitate = localitate;
	}

	public boolean isOras() {
		return isOras;
	}

	public void setOras(boolean isOras) {
		this.isOras = isOras;
	}

	public int getRazaKm() {
		return razaKm;
	}

	public void setRazaKm(int razaKm) {
		this.razaKm = razaKm;
	}

	public String getCoordonate() {
		return coordonate;
	}

	public void setCoordonate(String coordonate) {
		this.coordonate = coordonate;
	}
	
	

}
