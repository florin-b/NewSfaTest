package my.logon.screen.beans;


public class ValoriComanda {

	double total;
	double pondereB;
	double marja;
	String moneda;

	public ValoriComanda() {
	}

	public double getTotal() {
		return total;
	}

	public void setTotal(double total) {
		this.total = total;
	}

	public double getPondereB() {
		return pondereB;
	}

	public void setPondereB(double pondereB) {
		this.pondereB = pondereB;
	}

	public double getMarja() {
		return marja;
	}

	public void setMarja(double marja) {
		this.marja = marja;
	}

	public String getMoneda() {
		return moneda;
	}

	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}
	
	

}
