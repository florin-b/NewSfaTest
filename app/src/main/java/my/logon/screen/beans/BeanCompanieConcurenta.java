package my.logon.screen.beans;

public class BeanCompanieConcurenta {

	private String cod;
	private String nume;

	public String getCod() {
		return cod;
	}

	public void setCod(String cod) {
		this.cod = cod;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}

	public String toString() {
		return "BeanCompanieConcurenta [cod=" + cod + ", nume=" + nume + "]";
	}

}
