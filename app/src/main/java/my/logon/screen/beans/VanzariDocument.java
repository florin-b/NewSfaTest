package my.logon.screen.beans;

public class VanzariDocument {

	private String numeClient;
	private String codClient;
	private String numeAgent;
	private String nrDocument;
	private String dataDocument;
	private String valoareDocument;

	public VanzariDocument() {

	}

	public String getNumeClient() {
		return numeClient;
	}

	public void setNumeClient(String numeClient) {
		this.numeClient = numeClient;
	}

	public String getCodClient() {
		return codClient;
	}

	public void setCodClient(String codClient) {
		this.codClient = codClient;
	}

	public String getNumeAgent() {
		return numeAgent;
	}

	public void setNumeAgent(String numeAgent) {
		this.numeAgent = numeAgent;
	}

	public String getNrDocument() {
		return nrDocument;
	}

	public void setNrDocument(String nrDocument) {
		this.nrDocument = nrDocument;
	}

	public String getDataDocument() {
		return dataDocument;
	}

	public void setDataDocument(String dataDocument) {
		this.dataDocument = dataDocument;
	}

	public String getValoareDocument() {
		return valoareDocument;
	}

	public void setValoareDocument(String valoareDocument) {
		this.valoareDocument = valoareDocument;
	}

}
